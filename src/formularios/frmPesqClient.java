        private void preencherTabela(){
        String titulos[] =  {"ID Cliente", "Nome", "Sobre Nome", "Email"};
        String registro[] = new String[7];
        mTablela = new DefaultTableModel(null, titulos);
        // preenche a tabela de cliente se nenhum pesquisa tivert sido feita ainda
        if(txtPesqClient.getText().equals("")){
            for(int i =0; i < msDados.numeroClientes(); i++) {
                registro[0] = msDados.getClientes()[i].getIdCliente();
                registro[1] = msDados.getClientes()[i].getNome();
                registro[2] = msDados.getClientes()[i].getSobreNome();
                registro[3] = msDados.getClientes()[i].getEmail();
                mTablela.addRow(registro);
            } 
            tblCliente.setModel(mTablela);
            return;
        }
        // preenche a tabela se o inpute de radio nome for selecionado, a pessoa pode pesquisar de acordo com o nome
            if (radioNome.isSelected()){
                for(int i =0; i < msDados.numeroClientes(); i++) {
                 if(msDados.getClientes()[i].getNome().startsWith(txtPesqClient.getText())){
                      registro[0] = msDados.getClientes()[i].getIdCliente();
                      registro[1] = msDados.getClientes()[i].getNome();
                      registro[2] = msDados.getClientes()[i].getSobreNome();
                      registro[3] = msDados.getClientes()[i].getEmail();
                      mTablela.addRow(registro);

                 }
                }
            tblCliente.setModel(mTablela);
            return;
            }  
        // preenche a tabela se o inpute de radio SobreNome for selecionado, a pessoa pode pesquisar de acordo com o Sobre Nome
            if (radioSobreNome.isSelected()){
                for(int i =0; i < msDados.numeroClientes(); i++) {
                 if(msDados.getClientes()[i].getSobreNome().startsWith(txtPesqClient.getText())){
                      registro[0] = msDados.getClientes()[i].getIdCliente();
                      registro[1] = msDados.getClientes()[i].getNome();
                      registro[2] = msDados.getClientes()[i].getSobreNome();
                      registro[3] = msDados.getClientes()[i].getEmail();
                      mTablela.addRow(registro);

                 }
                }
            tblCliente.setModel(mTablela);
            return;
            }  
        // preenche a tabela se o inpute de radio IdCliente for selecionado, a pessoa pode pesquisar de acordo com o IdCliente   
            if (radioID.isSelected()){
                for(int i =0; i < msDados.numeroClientes(); i++) {
                 if(msDados.getClientes()[i].getIdCliente().startsWith(txtPesqClient.getText())){
                      registro[0] = msDados.getClientes()[i].getIdCliente();
                      registro[1] = msDados.getClientes()[i].getNome();
                      registro[2] = msDados.getClientes()[i].getSobreNome();
                      registro[3] = msDados.getClientes()[i].getEmail();
                      mTablela.addRow(registro);

                 }
                }
            tblCliente.setModel(mTablela);
            return;
            }  
    }

