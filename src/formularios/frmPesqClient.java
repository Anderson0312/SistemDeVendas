    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        int num = tblCliente.getRowCount();
        if(num == 0){
            resposta = "";
            dispose();
            return;
        }
        for (int i = 0; i<num; i++) {
            if(tblCliente.isRowSelected(i)){
                resposta = Utilidades.objectToString(tblCliente.getValueAt(i, 0));
                dispose();
                return;    
            }
        }
         resposta = Utilidades.objectToString(tblCliente.getValueAt(0, 0));
         dispose();
    }//GEN-LAST:event_btnPesquisarActionPerformed

