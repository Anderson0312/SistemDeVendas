    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(cmbProduto.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(rootPane,"Selecionar um PRODUTO");
            cmbProduto.requestFocusInWindow();
            return;
        }
        
        try {
            DefaultTableModel modelo = (DefaultTableModel)tblDetalhesVenda.getModel();
            int Linha = tblDetalhesVenda.getRowCount();
            for(int i = 0; Linha> i; i++){
                String idTabela = Utilidades.objectToString(tblDetalhesVenda.getValueAt(i, 0));
                var idCmb = cmbProduto.getSelectedIndex();
                if(idTabela.equals(""+idCmb)){
                    modelo.removeRow(i);
                    totais();
                    return;
                }
                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

