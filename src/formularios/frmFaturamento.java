    private void btnSalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvaActionPerformed
        // validaçoes para adicionar os produtos ao formulario
        if(cmbProduto.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(rootPane,"Selecionar um PRODUTO");
            cmbProduto.requestFocusInWindow();
            return;
        }
        
        if(txtQuanti.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane,"Selecionar uma Quantidade");
            cmbProduto.requestFocusInWindow();
            return;
        }
        
        if (!Utilidades.isNumeric(txtQuanti.getText())) {
            JOptionPane.showMessageDialog(rootPane,"Favor Digitar somente numeros.");
            txtQuanti.setText("");
            txtQuanti.requestFocusInWindow();
            return;
        }
        
        int quantidade = Integer.parseInt(txtQuanti.getText());
        if (quantidade <= 0){
            JOptionPane.showMessageDialog(rootPane,"Favor Digitar um numero acima de zero.");
            txtQuanti.setText("");
            txtQuanti.requestFocusInWindow();
            return;
        }
        
        // voltar aqui para RESOLVER O ERRO!!!!!  (Problema resolvido com ganbiarra, coloquei para selecionar pelo index no combo box em vez de selecionar pelo valor do prod)
        // A pessoa tem que colocar os id do prod na ordem crescente se n da ruim
        var cmbProd = cmbProduto.getSelectedIndex();
        //System.out.println( cmbProd + "formularios.frmFaturamento.btnSalvaActionPerformed()");
        int pos; // metodo para pegar o produto de acordo com a posição dele;
        pos = msDados.posicaoProduto(""+cmbProd);
        
        
        //Preenche com os valores escolhidos
        String registro[] = new String[5];
        registro[0] = msDados.getProdutos()[pos].getIdProduto();
        registro[1] = msDados.getProdutos()[pos].getDescircao();
        registro[2] = "" + msDados.getProdutos()[pos].getPreco();
        registro[3] = "" + quantidade;
        registro[4] = "" + (quantidade * msDados.getProdutos()[pos].getPreco());
        mTablela.addRow(registro);
        
        cmbProduto.setSelectedIndex(0);
        txtQuanti.setText("");
        cmbProduto.requestFocusInWindow();
        totais();
    }//GEN-LAST:event_btnSalvaActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(cmbCliente.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(rootPane,"Favor Selecionar um cliente!");
            cmbCliente.requestFocusInWindow();
            return;
        }
        
        if(txtQuantTot.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane,"Favor adicionar um Produto para Salvar");
            return;
        }
        
        int resposta = JOptionPane.showConfirmDialog(rootPane,"Deseja realizar a venda?");
        if (resposta != 0) {
            return;
        }
        
        int numFatura = msDados.getNumeroFatura()+1;
        
        FileWriter fw = null;
        PrintWriter pw = null;
            try {
                fw = new FileWriter("Data/Fatura.txt", true);
                pw = new PrintWriter(fw);
                
                var opcItemVal = (cmbCliente.getSelectedItem().toString());
                //System.out.println("360 ---->" + opcItemVal);
                
                var auxiliar = "1|"
                        +numFatura + "|"
                        +opcItemVal +"|"
                        +txtData.getText();
                
                
                pw.println("----------------------------");                      
                pw.println(auxiliar);
                pw.println("----------------------------");
             
                
            int num = tblDetalhesVenda.getRowCount();
            
            for (int i = 0; i<num; i++) {
                auxiliar = "2|"
                    + Utilidades.objectToString(tblDetalhesVenda.getValueAt(i, 0))+"|"
                    + Utilidades.objectToString(tblDetalhesVenda.getValueAt(i, 1))+"|"
                    + Utilidades.objectToString(tblDetalhesVenda.getValueAt(i, 2))+"|"
                    + Utilidades.objectToString(tblDetalhesVenda.getValueAt(i, 3))+"|"
                    + Utilidades.objectToString(tblDetalhesVenda.getValueAt(i, 4));
                
                pw.println(auxiliar);
            }
        

            } catch (Exception e1) {
                e1.printStackTrace();
                
            } finally{
                try {
                    if(fw != null) {
                        fw.close();
                    }
                } catch (Exception e2) {
                    e2.printStackTrace();
                }
            }
            
            JOptionPane.showMessageDialog(rootPane,"Venda " + numFatura+ " REALIZADA COM SUCESSO!");
            msDados.setNumeroFatura(numFatura);
            cmbCliente.setSelectedIndex(0);
            limparTablea();
            totais();
            cmbCliente.requestFocusInWindow();
            
    }//GEN-LAST:event_btnSalvarActionPerformed

