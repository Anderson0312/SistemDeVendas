    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        int num = tblProd.getRowCount();
        if(num == 0){
            resposta = "";
            dispose();
            return;
        }
        for (int i = 0; i<num; i++) {
            if(tblProd.isRowSelected(i)){
                resposta = Utilidades.objectToString(tblProd.getValueAt(i, 0));
                dispose();
                return;    
            }
        }
         resposta = Utilidades.objectToString(tblProd.getValueAt(0, 0));
         dispose();
    }//GEN-LAST:event_btnPesquisarActionPerformed

