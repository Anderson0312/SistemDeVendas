        private void preencherTabela(){
        String titulos[] =  {"ID Produto", "Descrição"};
        String registro[] = new String[2];
        mTablela = new DefaultTableModel(null, titulos);
        // preenche a tabela de cliente se nenhum pesquisa tivert sido feita ainda
        if(txtPesqProd.getText().equals("")){
            for(int i =0; i < msDados.numeroProdutos(); i++) {
                registro[0] = msDados.getProdutos()[i].getIdProduto();
                registro[1] = msDados.getProdutos()[i].getDescircao();
                mTablela.addRow(registro);
            } 
            tblProd.setModel(mTablela);
            return;
        }
        // preenche a tabela se o inpute de radio nome for selecionado, a pessoa pode pesquisar de acordo com o nome
            if (radioDescricao.isSelected()){
                for(int i =0; i < msDados.numeroProdutos(); i++) {
                 if(msDados.getProdutos()[i].getDescircao().startsWith(txtPesqProd.getText())){
                      registro[0] = msDados.getProdutos()[i].getIdProduto();
                      registro[1] = msDados.getProdutos()[i].getDescircao();
                      mTablela.addRow(registro);
                 }
                }
            tblProd.setModel(mTablela);
            return;
            }  
        
        // preenche a tabela se o inpute de radio IdCliente for selecionado, a pessoa pode pesquisar de acordo com o IdCliente   
            if (radioID.isSelected()){
                for(int i =0; i < msDados.numeroClientes(); i++) {
                 if(msDados.getProdutos()[i].getIdProduto().startsWith(txtPesqProd.getText())){
                      registro[0] = msDados.getProdutos()[i].getIdProduto();
                      registro[1] = msDados.getProdutos()[i].getDescircao();
                      mTablela.addRow(registro);

                 }
                }
            tblProd.setModel(mTablela);
            return;
            }  
    }

